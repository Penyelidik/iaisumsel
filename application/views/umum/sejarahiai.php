                <p style="text-align: justify;" class="font-15 wow bounceInLeft" data-wow-delay=".3s">
                  Pada waktu Indonesia merdeka, hanya ada satu orang akuntan pribumi, yaitu Dr. Abutari sedangkan Prof. Soemardjo lulus pendidikan akuntan di negeri Belanda pada tahun 1956.<br><br>
                  Akuntan-akuntan Indonesia pertama lulusan dalam negeri adalah <b>Basuki Siddharta, Hendra Darmawan, Tan Tong Djoe,</b> dan <b>Go TIe Siem</b>, mereka lulus pertengahan tahun 1957. Keempat akuntan ini bersama dengan Prof. Soemardjo mengambil prakarsa mendirikan perkumpulan akuntan untuk bangsa Indonesia saja. Alasannya, mereka tidak mungkin menjadi anggota NIVA (Nederlands Institute Van Accountants) dan berpendapat tidak mungkin kedua lembaga itu akan memikirkan perkembangan dan pembinaan akuntan Indonesia.<br><br>
                  Hari Kamis, 17 Oktober 1957, kelima akuntan tadi mengadakan pertemuan di aula Universitas Indonesia (UI) dan bersepakat untuk mendirikan perkumpulan akuntan Indonesia. Karena pertemuan tersebut tidak dihadiri oleh semua akuntan yang ada maka diputuskan membentuk Panita Persiapan Pendirian Perkumpulan Akuntan Indonesia. Panitia diminta untuk menghubungi akuntan lainnya untuk menyatakan pendapat mereka. Dalam hal itu Prof. Soemardjo duduk sebagai ketua, Fo Tie Siem sebagai penulis, Basuki SIddharta sebagai bendahara sedangkan Hendar Darmawan dan Tan Tong Djoe sebagai komisaris. Surat yang dikirimkan Panitia kepada 6 akuntan lainnya memperoleh jawaban setuju. <br><br>
                  Perkumpulan yang akhirnya diberi nama Ikatan Akuntan Indonesia (IAI) akhirnya berdiri pada 23 Desember 1957, yaitu pada pertemuan ketiga yang diadakan di aula UI pada pukul 19.30 WIB.<br><br>

              <p style="text-align: justify;" class="font-15 wow bounceInLeft" data-wow-delay=".3s">
                Untuk mencapai maksud, tujuan, dan fungsinya, IAI melaksanakan beragam kegiatan diantaranya :<br> - Pendaftaran dan pelayanan keanggotaan<br> - Pengembangan dan penyusunan standar akuntansi keuangan<br> - Pengembangan dan penegakkan kode etik akuntan<br> - Pemberian konsultasi untuk pengembangan usaha kecil, menengah dan koperasi<br> - Publikasi<br> - Hubungan internasional<br> - Menjadi pusat pengetahuan dan pengembangan akuntansi<br> - Menjaga dan meningkatkan kompetensi akuntan melalui kegiatan pendidikan dan pelatihan<br> - Melaksanakan sertifikasi di bidang akuntansi sebagai tolak ukur standar kualitas keprofesian<br> - Menjaga kepercayaan pemakai jasa dan masyarakat luas atas hasil kerja profesi akuntan yang tergabung dalam IAI. <br> <br>

                IAI bermaksud menghimpun potensi Akuntan Indonesia untuk menjadi penggerak pembangunan nasional dalam mewujudkan masyarakat adil dan makmur berdasarkan Pancasila dan UUD 1945. IAI bertujuan mengembangkan dan mendayagunaakan potensi Akuntan Indonesia sehingga terbentuk suatu cipta dan karya Akuntan Indonesia untuk didarmabaktikan bagi kepentingan bangsa dan negara. IAI berfungsi sebagai wadah komunikasi yang menjebatani berbagai latar belakang tugas dan bidang pengabdiannya untuk menjalin kerjasama yang bersifat sinergi secara serasi, seimbang dan selaras. <br><br>
                Anggaran Dasar dan Anggaran Rumah Tangga (AD ART) IAI telah mengalami beberapa kali perubahan, yang terakhir adalah AD ART yang ditetapkan pada Kongres Luar Biasa (KLB) IAI tanggal 27 Juni 2012 sesuai Keputusan Sidang Pleno Tetap KLB  IAI Tahun 2012 Nomor: 05/Kongres Luar Biasa/IAI/VI/2012. <br><br>
                Saat ini IAI merupakan satu-satunya wadah yang mewakili profesi akuntan Indonesia secara keseluruhan. IAI merupakan anggota International Federation of Accountants, organisasi profesi akuntan dunia yang merepresentasikan lebih 2,5 juta akuntan yang bernaung dalam 167 asosiasi profesi akuntan yang tersebar di 127 negara. Sebagai anggota IFAC, IAI memiliki komitmen untuk melaksanakan semua standar internasional yang ditetapkan demi kualitas tinggi dan penguatan profesi akuntan di Indonesia. IAI juga merupakan anggota sekaligus pendiri ASEAN Federation of Accountants (AFA). Saat ini IAI menjadi sekretariat permanen AFA. <br><br> </p>


                  <p style="text-align: justify;" class="font-15 wow bounceInLeft" data-wow-delay=".3s"><b>Keanggotaan </b> <br>
                  Pada awalnya keanggotaan IAI adalah perseorangan yang diarahkan untuk memilih Kompartemen sesuai bidang kerja anggota. Anggota IAI bergabung dalam 4 (empat) Kompartemen yang dibentuk IAI kala itu, yaitu IAI Kompartemen Akuntan Publik (IAI KAP), IAI Kompartemen Akuntan Pendidik (IAI KAPd), IAI Kompartemen Akuntan Manajemen (IAI KAM), dan IAI Kompartemen Akuntan Sektor Publik (IAI KASP).  <br><br>
                  Pada tanggal 23 Mei 2007 IAI melaksanakan Kongres Luar Biasa (KLB) yang memutuskan IAI adalah organisasi perofesi yang beranggotakan Perseorangan dan Asosiasi.<br><br>
                  IAI KAP merubah formatnya menjadi asosiasi akuntan publik independen dengan nama Institut Akuntan Publik Indonesia (IAPI) pada tanggal 24 Mei 2007. IAI KAP dibubarkan sesuai surat keputusan DPN IAI tanggal 4 Juni 2007 Nomor Kep-22/SK/DPN/IAI/V/2007. Selanjutnya IAPI pada saat yang sama ditetapkan menjadi anggota asosiasi IAI sesuai surat keputusan DPN IAI tanggal 4 Juni 2007 nomor Kep-23/SK/DPN/IAI/V/2007.<br><br>
                  IAI KAM juga merubah formatnya menjadi asosiasi independen dengan nama Institut Akuntan Manajemen Indonesia (IAMI). Pada tanggal 1 September 2009 IAI KAM dibubarkan, dan IAMI ditetapkan menjadi anggota asosiasi IAI sesuai surat keputusan DPN IAI nomor Kep-72/SK/DPN/IAI/IX/2009.<br><br>
                  Pada saat Kongres XI IAI dilaksanakan 10 Desember 2012, DPN IAI Periode 2010-2014 diberi amanah untuk mengkaji usulan perubahan keanggotaan IAI serta berwenang menentukan tindakan berikutnya.
                  Kongres Luar Biasa IAI dilaksanakan pada tanggal 27 Juni 2012 dengan keputusan IAI adalah organisasi profesi yang beranggotakan perseorangan, terdiri dari Anggota Utama, Anggota Madya dan Anggota Muda.</p>

             




                

                
                   

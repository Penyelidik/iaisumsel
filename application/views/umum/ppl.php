                <h2 class="font-16 semi-font fables-second-hover-color-link text-center fables-light-gary-background  p-3 mb-2 ">  Pendidikan Profesional Berkelanjutan </h2>
                <p style="text-align: justify;" class="font-15">
                  Pendidikan Profesional Berkelanjutan (PPL) adalah kegiatan belajar terus menerus (continuous learning) yang harus ditempuh oleh akuntan profesional agar senantiasa dapat memelihara, meningkatkan dan megembangkan kompetensi profesionalnya.<br><br>
                  Tujuan PPL adalah untuk mendorong akuntan profesional memelihara, meningkatkan dan mengembangkan kompetensi profesionalnya secara berkesinambungan, membekali akuntan profesional dengan pengetahuan dan keahlian mutakhir di bidangnya sehingga mampu menerapkannya dengan baik dalam menjalankan tugas dan kewajiban profesional mereka, serta menjaga dan meningkatkan kepercayaan masyarakat kepada profesi akuntan dengan menunjukan bahwa akuntan memiliki standar kompetensi profesional sesuai dengan harapan masyarakat pengguna jasa.
                   </p>
                   <br>
                   <h2 class="font-16 semi-font fables-second-hover-color-link text-center fables-light-gary-background  p-3 mb-2 ">  Ketentuan Umum </h2>
                <p style="text-align: justify;" class="font-15">
                  Akuntan profesional harus selalu mengupdate keilmuan dan pengetahuan tentang standar akuntansi terbaru agar selalu bisa memenuhi tuntutan publik pengguna jasa. Dinamika akuntansi yang terus berkembang mengikuti perkembangan zaman, menuntut para Akuntan Profesional untuk tetap bisa menjaga profesionalismenya. Akuntan manapun yang tidak mengupdate diri akan menjadi obsolete dan jasa yang diberikan tidak lagi relevan. Dalam rangka menjaga profesionalisme para akuntan, Peraturan Menteri Keuangan (PMK) Nomor 25/PMK.01/2014 tentang Akuntan Beregister Negara mewajibkan Akuntan Profesional mengikuti Pendidikan Profesional Berkelanjutan (PPL) yang diselenggarakan Ikatan Akuntan Indonesia dan institusi lain yang diakui. Langkah ini sejalan dengan ketentuan International Federation of Accountants (IFAC) dan best practice yang diterima secara global. <br><br>
                  Anggaran Dasar dan Anggaran Rumah Tangga IAI telah mengatur dan menetapkan bahwa seluruh Anggota IAI berkewajiban mengikuti PPL. Peraturan Menteri Keuangan (PMK) Nomor 25/PMK.01/2014 tentang Akuntan Beregister Negara mewajibkan seluruh Akuntan untuk menjaga kompetensi melalui Pendidikan Profesional Berkelanjutan (PPL) dan menyampaikan laporan realisasi PPL kepada Ikatan Akuntan Indonesia (IAI). Pasal 16 PMK 25/PMK.01/2014 antara lain menyebutkan, PPL dapat ditempuh melalui kegiatan PPL yang diselenggarakan oleh IAI, PPPK, dan/atau pihak lain yang diakui IAI dan/atau PPPK. Akuntan wajib mengikuti PPL paling sedikit berjumlah 30 (tiga puluh) Satuan Kredit PPL (SKP) setiap tahun. Sebagai anggota International Federation of Accountants (IFAC), IAI mewajibkan 120 SKP bagi setiap Akuntan selama 3 (tiga) tahun.

                   </p>
                   <br>
                   <h2 class="font-16 semi-font fables-second-hover-color-link text-center fables-light-gary-background  p-3 mb-2 ">  Ketentuan Kewajiban SKP bagi Anggota </h2>
                <p style="text-align: justify;" class="font-15">

                  Anggota Utama IAI pemegang sebutan Chartered Accountant Indonesia wajib memelihara dan meningkatkan kompetensi melalui kegiatan PPL yang diselenggarakan oleh IAI, atau pihak lain yang diakui IAI. Kegiatan PPL yang dimaksud adalah : <br>
                   <ol type="a" class="font-15 ">
                   
                   <li class="font-15 ">Pelatihan, kursus, lokakarya/workshop, diskusi panel, seminar, konferensi, konvensi atau simposium.</li>
                   <li class="font-15 ">Program pascasarjana pada bidang studi yang relcvan dengan kompetensi akuntan.</li>
                   <li class="font-15 ">Program belajar jarak jauh yang relevan dengan kompetensi akuntan.</li>
                   <li class="font-15 ">Penulisan artikel, majalah, atau buku dengan materi yang relevan dengan kompetensi akuntan dan dipublikasikan</li>
                   <li class="font-15 ">Riset profesional atau studi terhadap bidang-bidang yang relevan dengan kompetensi akuntan.</li>
                   <li class="font-15 ">Menjadi anggota Dewan/Komite Teknis IA1 atau organisasi profesi lainnya yang diakui IAI yang mengharuskan yang bersangkutan menyiapkan atau mereview materi-materi yang bersifat teknis yang relevan dengan kompetensi akuntan.</li>
                 </ol>
                 <p style="text-align: justify;" class="font-15">
                  Jumlah Satuan Kredit PPI. (SKP) yang wajib diikuti oleh Anggota Utama IAI pemegang sebutan Chartered Accountant Indonesia paling sedikit berjumlah 120 (seratus dua puluh) SKP dalam periode 3 (tiga) tahun. Dalam memenuhi kewajiban, Anggota Utama IAI pemegang sebutan Chartered Accountant Indonesia harus memenuhi sekurang-kurangnya 30 (tiga puluh) SKP dalam 1 (satu) tahun. Anggota Utama IAI pemegang sebutan Chartered Accountant Indonesia wajib menyampaikan laporan realisasi PPL tahunan dengan lengkap kepada IAI pada akhir bulan Januari tahun berikutnya.

                   </p>
